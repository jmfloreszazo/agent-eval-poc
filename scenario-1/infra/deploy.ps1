# Deploys Azure OpenAI for the PoC and writes credentials to ../../.env
# Usage (from repo root):
#   .\scenario-1\infra\deploy.ps1                      # use defaults
#   .\scenario-1\infra\deploy.ps1 -ResourceGroup rg-eval-poc -Location eastus2

param(
    [string]$SubscriptionId = '',                       # empty = current `az account`
    [string]$ResourceGroup = 'rg-agent-eval-poc',
    [string]$Location      = 'eastus2'
)

$ErrorActionPreference = 'Stop'
# scenario-1/infra/ -> scenario-1/ -> repo root
$repoRoot = Split-Path -Parent (Split-Path -Parent $PSScriptRoot)

Write-Host '==> Checking az login...' -ForegroundColor Cyan
$account = az account show 2>$null | ConvertFrom-Json
if (-not $account) {
    Write-Host 'No session. Run `az login` and re-run this script.' -ForegroundColor Red
    exit 1
}

if ($SubscriptionId) {
    az account set --subscription $SubscriptionId | Out-Null
    $account = az account show | ConvertFrom-Json
}
Write-Host ("    Subscription: {0} ({1})" -f $account.name, $account.id)

Write-Host "==> Resource group $ResourceGroup in $Location..." -ForegroundColor Cyan
az group create --name $ResourceGroup --location $Location --only-show-errors | Out-Null

Write-Host '==> Deploying Bicep (Azure OpenAI + gpt-4o-mini)...' -ForegroundColor Cyan
$deployName = "agent-eval-poc-{0:yyyyMMddHHmmss}" -f (Get-Date)
$deployJson = az deployment group create `
    --resource-group $ResourceGroup `
    --name $deployName `
    --template-file (Join-Path $PSScriptRoot 'main.bicep') `
    --parameters location=$Location `
    --only-show-errors `
    -o json | ConvertFrom-Json

$endpoint       = $deployJson.properties.outputs.endpoint.value
$accountName    = $deployJson.properties.outputs.accountName.value
$deploymentName = $deployJson.properties.outputs.deploymentName.value

Write-Host '==> Fetching API key...' -ForegroundColor Cyan
$key = az cognitiveservices account keys list `
    --resource-group $ResourceGroup `
    --name $accountName `
    --query key1 -o tsv

$envPath = Join-Path $repoRoot '.env'
$envContent = @"
# Generated by infra/deploy.ps1 on $(Get-Date -Format 'yyyy-MM-dd HH:mm')
USE_AZURE_OPENAI=YES
AZURE_OPENAI_ENDPOINT=$endpoint
AZURE_OPENAI_API_KEY=$key
AZURE_DEPLOYMENT_NAME=$deploymentName
AZURE_MODEL_NAME=gpt-4o-mini
AZURE_MODEL_VERSION=2024-07-18
OPENAI_API_VERSION=2024-10-21
"@
Set-Content -Path $envPath -Value $envContent -Encoding utf8

Write-Host ''
Write-Host '==> DONE' -ForegroundColor Green
Write-Host ("    Endpoint:    {0}" -f $endpoint)
Write-Host ("    Deployment:  {0}" -f $deploymentName)
Write-Host ("    .env:        {0}" -f $envPath)
Write-Host ''
Write-Host 'Next step: cd scenario-1; pytest evals/test_agent_trajectory.py -v'
