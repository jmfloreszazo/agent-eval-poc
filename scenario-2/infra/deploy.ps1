# Deploys scenario-2 infra (Foundry + App Insights + Log Analytics + gpt-4o-mini)
# and writes credentials to ../../.env.scenario-2 (kept separate from scenario-1).
#
# Usage (from repo root):
#   .\scenario-2\infra\deploy.ps1
#   .\scenario-2\infra\deploy.ps1 -ResourceGroup rg-aieval2-poc -Location eastus2

param(
    [string]$SubscriptionId = '',                       # empty = current `az account`
    [string]$ResourceGroup  = 'rg-aieval2-poc',
    [string]$Location       = 'eastus2'
)

$ErrorActionPreference = 'Stop'
# scenario-2/infra/ -> scenario-2/ -> repo root
$repoRoot = Split-Path -Parent (Split-Path -Parent $PSScriptRoot)

Write-Host '==> Checking az login...' -ForegroundColor Cyan
$account = az account show 2>$null | ConvertFrom-Json
if (-not $account) {
    Write-Host 'No session. Run `az login` and re-run this script.' -ForegroundColor Red
    exit 1
}

if ($SubscriptionId) {
    az account set --subscription $SubscriptionId | Out-Null
    $account = az account show | ConvertFrom-Json
}
Write-Host ("    Subscription: {0} ({1})" -f $account.name, $account.id)

# Object id of the signed-in principal -> used for RBAC on the Foundry account.
$principalObjectId = az ad signed-in-user show --query id -o tsv 2>$null
if (-not $principalObjectId) {
    Write-Host 'WARNING: Could not resolve signed-in user object id. Skipping RBAC.' -ForegroundColor Yellow
    $principalObjectId = ''
}
Write-Host ("    Principal:    {0}" -f $principalObjectId)

Write-Host "==> Resource group $ResourceGroup in $Location..." -ForegroundColor Cyan
az group create --name $ResourceGroup --location $Location --only-show-errors | Out-Null

Write-Host '==> Deploying Bicep (Foundry + App Insights + gpt-4o-mini)...' -ForegroundColor Cyan
$deployName = "aieval2-{0:yyyyMMddHHmmss}" -f (Get-Date)
$deployJson = az deployment group create `
    --resource-group $ResourceGroup `
    --name $deployName `
    --template-file (Join-Path $PSScriptRoot 'main.bicep') `
    --parameters location=$Location principalObjectId=$principalObjectId `
    --only-show-errors `
    -o json | ConvertFrom-Json

$o = $deployJson.properties.outputs
$foundryAccount    = $o.foundryAccountName.value
$foundryEndpoint   = $o.foundryEndpoint.value
$projectName       = $o.projectName.value
$projectEndpoint   = $o.projectEndpoint.value
$deploymentName    = $o.deploymentName.value
$appiConnString    = $o.appInsightsConnectionString.value
$lawCustomerId     = $o.logAnalyticsWorkspaceId.value
$lawResourceId     = $o.logAnalyticsResourceId.value
$projectMiId       = $o.projectPrincipalId.value

Write-Host '==> Fetching Foundry API key (fallback for SDKs without Entra)...' -ForegroundColor Cyan
$key = az cognitiveservices account keys list `
    --resource-group $ResourceGroup `
    --name $foundryAccount `
    --query key1 -o tsv

$envPath = Join-Path $repoRoot '.env.scenario-2'
$envContent = @"
# Generated by scenario-2/infra/deploy.ps1 on $(Get-Date -Format 'yyyy-MM-dd HH:mm')
# ---- Azure AI Foundry ----
AZURE_AI_FOUNDRY_ACCOUNT=$foundryAccount
AZURE_AI_FOUNDRY_ENDPOINT=$foundryEndpoint
AZURE_AI_PROJECT_NAME=$projectName
AZURE_AI_PROJECT_ENDPOINT=$projectEndpoint
AZURE_AI_FOUNDRY_API_KEY=$key
AZURE_OPENAI_DEPLOYMENT=$deploymentName
AZURE_OPENAI_API_VERSION=2024-10-21
# ---- Observability ----
APPLICATIONINSIGHTS_CONNECTION_STRING=$appiConnString
LOG_ANALYTICS_WORKSPACE_ID=$lawCustomerId
LOG_ANALYTICS_RESOURCE_ID=$lawResourceId
AZURE_RESOURCE_GROUP=$ResourceGroup
AZURE_SUBSCRIPTION_ID=$($account.id)
# ---- Hosted agent (continuous eval) ----
AZURE_AI_AGENT_NAME=exp2-finops-agent
AZURE_AI_MODEL_DEPLOYMENT_NAME=$deploymentName
AZURE_AI_PROJECT_MI_PRINCIPAL_ID=$projectMiId
"@
Set-Content -Path $envPath -Value $envContent -Encoding utf8

Write-Host ''
Write-Host '==> DONE' -ForegroundColor Green
Write-Host ("    Foundry endpoint:  {0}" -f $foundryEndpoint)
Write-Host ("    Project endpoint:  {0}" -f $projectEndpoint)
Write-Host ("    Deployment:        {0}" -f $deploymentName)
Write-Host ("    .env file:         {0}" -f $envPath)
Write-Host ''
Write-Host 'Next steps:'
Write-Host '  1. cd scenario-2'
Write-Host '  2. ..\.venv\Scripts\python.exe -m pip install -r requirements.txt'
Write-Host '  3. ..\.venv\Scripts\python.exe src/agent.py        # produces traces.jsonl + AppInsights traces'
Write-Host '  4. ..\.venv\Scripts\python.exe evals/run_quality.py'
Write-Host '  5. ..\.venv\Scripts\python.exe evals/run_safety.py'
Write-Host '  6. ..\.venv\Scripts\python.exe evals/run_governance.py'
